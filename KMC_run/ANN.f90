   SUBROUTINE FindSizeWeightsArray(ANN,iprint)
   !Finds the size of the weight vector by computing nweights and nbias using 
   !  ndimensionsx, nneuronperlayer and nlayers
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: nlayers,ndimx,nneurons
      INTEGER, DIMENSION(:), POINTER :: nneuronperlayer
      INTEGER :: ilayer,nweights,nbias,iprint1
      INTEGER, OPTIONAL :: iprint
      
      IF (PRESENT(iprint)) THEN
         iprint1=iprint
      ELSE
         iprint1=1
      END IF
      
      ndimx=ANN%ndimensionsx
      IF (ndimx==0) THEN
         WRITE(6,*) "Err>> ndimensionsx in FindSizeWeightsArray is zero"
         STOP
      END IF
      nlayers=ANN%nlayers
      IF (nlayers==0) THEN
         WRITE(6,*) "Err>> nlayers in FindSizeWeightsArray is zero"
         STOP
      END IF
      nneuronperlayer=>ANN%nneuronperlayer
      IF (ASSOCIATED(ANN%nneuronperlayer)) THEN
         IF (SIZE(nneuronperlayer)/=nlayers) THEN
            WRITE(6,*) "Err>> Number of layers and size of nneuronperlayer are incompatible in FindSizeWeightsArray"
            STOP
         END IF
      ELSE
         WRITE(6,*) "Err>> nneuronperlayer is not associated in FindSizeWeightsArray"
         STOP
      END IF
      
      nweights=0
      nbias=0
      DO ilayer=1,nlayers
         nneurons=nneuronperlayer(ilayer)
         IF (nneurons<=0 .OR. nneurons>MaxNeuronPerLayer) THEN
            WRITE(6,*) "Err>> nneurons in FindSizeWeightsArray is out of bounds:",nneurons
            STOP
         END IF
         nweights=nweights+nneurons*ndimx
         nbias=nbias+nneurons
         ndimx=nneurons !this is the dimensionality of the new layer
      END DO
      ANN%nweights=nweights
      ANN%nbias=nbias
      IF (iprint1>0) THEN
         WRITE(6,*) "Creating weights array ..."
         WRITE(UNIT=6,FMT='(" ... number of weights in ANN: ",I4)') ANN%nweights
         WRITE(UNIT=6,FMT='(" ... number of biases in ANN: ",I4)') ANN%nbias
      END IF
      CALL MakeSizeANNWeights(ANN,nweights+nbias)
   END SUBROUTINE FindSizeWeightsArray
