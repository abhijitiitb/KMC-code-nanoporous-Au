   SUBROUTINE MakeSizeANNWeights(ANN,nweightsbias,iprint)
   !creates weights array of size nweights+nbias
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: nweightsbias,iprint1
      INTEGER, OPTIONAL :: iprint
      
      IF (PRESENT(iprint)) THEN
         iprint1=iprint
      ELSE
         iprint1=1
      END IF
      
      IF (ASSOCIATED(ANN%weights)) THEN
         IF (SIZE(ANN%weights)/=nweightsbias) THEN
            DEALLOCATE(ANN%weights)
            ALLOCATE(ANN%weights(nweightsbias))
