   SUBROUTINE RecoverDataFromANNTable(ANN,fdata,recovered,signature)
   !tries to recover data from stored information in ANN table
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      REAL(dp) :: fdata
      LOGICAL :: recovered
      INTEGER :: signature,i
      INTEGER, DIMENSION(:), POINTER :: xsignature
      
      recovered=.FALSE.
      
      xsignature=>ANN%xsignature
      DO i=1,ANN%ndata
         recovered=(xsignature(i)==signature)
         IF (recovered) THEN
            fdata=ANN%ydata(i)
            RETURN
         END IF
      END DO
      
      xsignature=>ANN%xstoredsignature
      DO i=1,ANN%ndata_stored
         recovered=(xsignature(i)==signature)
