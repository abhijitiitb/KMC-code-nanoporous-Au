   FUNCTION ANNError(x,errorstatus)
   !x corresponds to the weights
   !ydata,xdata,ndimensionsx,ndata,weights,nweights,nbias,nneuronperlayer,nlayers,activationfunction
   !Obtains the error given the ydata, xdata and the weights
   !ANN is the weighted norm
   !ndata is the number of data points used in finding the error
      IMPLICIT NONE
      INTEGER :: activationfunction,nselected
      !REAL(dp), DIMENSION(:), POINTER :: fdata_selected
      REAL(dp), DIMENSION(:), INTENT(IN) :: x
      REAL(dp) :: ANNError
      INTEGER :: i,j,i1,errorstatus
      
      errorstatus=0
      
      j=0
      ActiveNeuralNet%weights=0._dp
      DO i=1,ActiveNeuralNet%nweights
         IF (ActiveNeuralNet%InteractionIsActive(i)) THEN
            j=j+1
            ActiveNeuralNet%weights(i)=x(j)
         END IF
      END DO
      DO i=ActiveNeuralNet%nweights+1,ActiveNeuralNet%nweights+ActiveNeuralNet%nbias
         j=j+1
         ActiveNeuralNet%weights(i)=x(j)
      END DO
      !ActiveNeuralNet%weights=x
      
      nselected=ActiveNeuralNet%nselected
      !fdata_selected=>ActiveNeuralNet%fdata_selected
      ActiveNeuralNet%fdata_selected=ANNOutput(ActiveNeuralNet, &
         ActiveNeuralNet%xdata_selected, &
         ActiveNeuralNet%nselected,ActiveNeuralNet%ndimensionsx)
       
      ANNError=0._dp
      ANNError=SUM((ActiveNeuralNet%fdata_selected(1:nselected)- &
         ActiveNeuralNet%ydata_selected(1:nselected))* &
         (ActiveNeuralNet%fdata_selected(1:nselected)- &
         ActiveNeuralNet%ydata_selected(1:nselected)))

      !symmetry constraints using Lagrange multipliers
      DO i=1,ActiveNeuralNet%nweights
         IF (ActiveNeuralNet%InteractionIsActive(i)) THEN
            j=j+1
            i1=ActiveNeuralNet%SymmMap(i)
            ANNError=ANNError+x(j)*(ActiveNeuralNet%weights(i)-ActiveNeuralNet%weights(i1))**2 !penalize if symmetry is not obeyed
         END IF
      END DO
   END FUNCTION ANNError
