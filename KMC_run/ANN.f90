   FUNCTION DataIsUnique1(ANN,signature)
   !TRUE when this data point is new to ANN, else it is false
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: signature,i
      INTEGER, DIMENSION(:), POINTER :: xsignature=>NULL()
      LOGICAL :: DataIsUnique1
      
      DataIsUnique1=.TRUE.
      xsignature=>ANN%xsignature
      DO i=1,ANN%ndata
         IF (xsignature(i)==signature) THEN
            DataIsUnique1=.FALSE.
            RETURN
         END IF
      END DO
      
      xsignature=>ANN%xstoredsignature
      DO i=1,ANN%ndata_stored
         IF (xsignature(i)==signature) THEN
            DataIsUnique1=.FALSE.
            RETURN
         END IF
      END DO
   END FUNCTION DataIsUnique1
