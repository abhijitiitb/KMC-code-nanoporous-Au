 MODULE ANNModule
   !Purpose: Train ANN and to evaluate ANN nodel response for a particular input

   !ydata -- the observables (1D array of size ndata) 
   !xdata -- input values in 1D form (1D array of size ndimensionsx*ndata)
   !ndimensionsx -- dimensionality of the independent variable space
   !ndata -- number of data points
   !nlayers -- number of layers in the ANN
   !nneuronperlayer -- Number of neuron in each layer (1D array of size nlayers)
   !weights -- weight given to each input in a neuron
   
   ! Weights are supplied by the user (or generated by the ANN training subroutine) in the 1D raw form 
   !     transpose of "weights" vector, which contains the weights of all the layers
   ![w(1,1) w(1,2) ....  w(1,m) w(2,1) w(2,2) ....  w(2,m) ..... w(n,1) w(n,2) ....  w(n,m) b(1) ... b(n)]
   ! These get transformed into the processed form
   !    _                            _
   !   |  w(1,1) w(1,2)  ...   w(1,m) |  
   !   |                              |  w(i,j) is the weight for the ith neuron acting on the jth element of the input
   ! W=|                              |  these are the weights for a particular layer I
   !   |                              |  each row corresponds to the weights for a particular neuron
   !   |                              |  n is the number of neurons in the layer I (nneuronperlayer(I))
   !   |_ w(n,1) w(n,1)  ...   w(n,m)_|  m is the dimensionality of input variables (ndimensionsx)
   !  and
   ! b=[b(1) b(2)  ..... b(m)]' is the bias vector for the neurons in the layer. Note that W and b are both in weights 
   !   vector
   
   !Data is supplied by the user in the 1D raw form
   !     transpose of [x(1,1) x(2,1) ....  x(m,1) w(1,2) x(2,2) ....  x(m,2) ..... x(1,p) x(2,p) ....  x(m,p)]
   ! These get transformed into the processed form
   !    _                            _
   !   |  x(1,1) x(1,2)  ...   x(1,p) |
   !   |                              |
   ! X=|                              |  these are the input data provided to the ANN
   !   |                              |  each column corresponds to data point
   !   |                              |  m is the dimensionality of input variables (ndimensionsx)
   !   |_ x(m,1) x(m,2)  ...   x(m,p)_|  p is the number of data points collected (ndata)
   
   !The dot product between W and a column j of X (i.e., the jth data point) corresponds to the input for 
   !each of the activation functions of the layer -- if the activation function F() acts on the result we
   !obtain a column vector which is the output from the layer for the jth data point
   
   !The dot product between W and X gives a matrix where each column is the input for the activation functions
   !of the layer for a data point. When F() operates on the resulting matrix we obtain the output for all data
   !points. Each column of this new matrix is the output for a data point from this layer
   
   !    _        _
   !   |  t(1,1)  |
   !   |          |
   ! T=|          |  these are ydata provided to ANN
   !   |          |  each element corresponds to an observable
   !   |          |  p is the number of data points collected (ndata)
   !   |_ t(p,1) _|

