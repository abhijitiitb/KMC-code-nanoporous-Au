   SUBROUTINE OptimizeANN(ANN)
   !Optimizes the weights of ANN
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: nweights,nbias
      REAL(dp) :: fret,ftol,gtol,xtol,SclFac
      INTEGER :: iter,omode,i,j
      REAL(dp), DIMENSION(2*ANN%nweightsactive+ANN%nbias) :: weights !factor of two arises because of symmetry constraints
      INTEGER :: errorstatus,ITMAX
      
      nweights=ANN%nweights
      nbias=ANN%nbias
      IF (nweights==0 .OR. nbias==0) THEN
         WRITE(6,*) "Err>> Number of weights/bias is zero in OptimizeANN"
         STOP
      END IF
      omode=ANN%OptimizationMode
      weights=0._dp !ANN%weights
      
      !IF (ASSOCIATED(weights)) THEN
      !   IF (SIZE(weights)/=nweights+nbias) THEN
      !      WRITE(6,*) "Err>> Size of weights array is incompatible with nweights and nbias in OptimizeANN"
      !      STOP
      !   END IF
      !ELSE
      !   WRITE(6,*) "Err>> Weights are not associated in OptimizeANN"
      !END IF
      
      ActiveNeuralNet=>ANN
      
      SELECT CASE(omode)
      CASE(1) !steepest descent
         SclFac=0.01_dp !to be used for scaling forces
         ftol=1.0e-9_dp
         gtol=2.0e-6_dp
         xtol=1.0e-5_dp
         CALL SteepestDescent(weights,ftol,gtol,xtol,iter,fret,ANNError,ANNErrorGradient, &
            dx=0.00001_dp,ITMAX=ITMAX,IsPrint1=.TRUE.,errorstatus=errorstatus)
      CASE(2) !conjugate gradient
         SclFac=0.01_dp
         ftol=1.0e-9_dp !this should come from potential
         gtol=2.0e-6_dp
         xtol=1.0e-5_dp
         CALL ConjugateGradient(weights,ftol,gtol,xtol,iter,fret,ANNError,ANNErrorGradient, &
            dx=0.0001_dp,ITMAX=ITMAX,IsPrint1=.TRUE.,errorstatus=errorstatus)
      CASE(3) !LBFGS
         ftol=1.0e-9_dp
         gtol=2.0e-3_dp
         xtol=1.0e-5_dp
         ITMAX=200
         CALL Lbfgs(weights,ftol,gtol,xtol,iter,fret,ANNError,ANNErrorGradient, &
              IsPrint1=.TRUE.,ITMAX=ITMAX,errorstatus=errorstatus)
      !CASE(4) !dynamical Euler
         !CALL 
      !CASE(5) !Runge Kutta
      !CASE(6) !QuickMin
      !CASE(7) !FIRE
      CASE(8) !DFP
         !ftol=1.0e-9_dp
         !gtol=2.0e-6_dp
         !xtol=1.0e-5_dp
         CALL DFP(weights,ftol,gtol,xtol,iter,fret,ANNError,ANNErrorGradient, &
            IsPrint1=.TRUE.,errorstatus=errorstatus)
      CASE DEFAULT
         WRITE(6,*) "Error>> Optimization type is not known to OptimizeANN"
         STOP
      END SELECT
      WRITE(6,*) "Optimized weights:"
      j=0
      ActiveNeuralNet%weights=0._dp
      DO i=1,ActiveNeuralNet%nweights
         IF (ActiveNeuralNet%InteractionIsActive(i)) THEN
            j=j+1
            ActiveNeuralNet%weights(i)=weights(j)
         END IF
      END DO
      DO i=ActiveNeuralNet%nweights+1,ActiveNeuralNet%nweights+ActiveNeuralNet%nbias
         j=j+1
         ActiveNeuralNet%weights(i)=weights(j)
      END DO
      DO i=1,ANN%nweights+ANN%nbias
         WRITE(6,*) ANN%weights(i)
      END DO
   END SUBROUTINE OptimizeANN
