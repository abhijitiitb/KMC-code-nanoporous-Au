   SUBROUTINE TrainANNBackpropagation(ANN,errorstatus)
   !Trains an ANN using the input data xdata and target data ydata
   !User inputs:
   !ydata is target data of size ndata
   !xdata is input data of size ndimensionsx * ndata
   !ndimensionsx is the dimensionality of x in each data point
   !ndata is the number of data points
   !nneuronperlayer is an array containing the number of neurons in each layer
   !nlayers is the number of layers in the ANN
   !weights is the weight vector which is automatically created by the code
   !nweights is the number of weights in the ANN
   !nbias is the number of bias in the ANN
   !ftraining and fvalidation are the fraction of data used for training and validation, respectively
   !datapurpose has value (1) training (2) testing and (3) validation
   !errorstatus is 0 (fitting was done normally), 1 (inadequate amount of data), 2 ()
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: purpose,errorstatus,i
      REAL(dp) :: RSq,maxerror
      CHARACTER :: crapchar
      
      !CALL CheckANNParameters() !makes sure that the code is able to cope with the sizes provided
      CALL FindSizeWeightsArray(ANN) !this will set weights to zero is the size of weights is changed
      ANN%weights=0._dp
      !IF (.NOT. ANN%IsFitted) CALL InitializeANNWeights(ANN) !if fitted used fitted weights for fast convergence
      CALL PartitionANNData(ANN)
      
      !Train the network
      purpose=1
      WRITE(6,*) "Training the network in TrainANNBackpropagation ..."
      CALL SelectData(ANN,purpose)!Collect the data into one array
      CALL GetActiveInteractions(ANN)
      
      IF (2*ANN%nweightsactive>ANN%nselected) THEN
         WRITE(6,*) "Err>> Too many parameters and few data points"
         errorstatus=4
         RETURN
      END IF
      
      CALL OptimizeANN(ANN)
      DO WHILE (EliminatedWeakInteractions(ANN,0.01_dp))
         CALL OptimizeANN(ANN)
         WRITE(6,*) "++++++++++++++++++++++++++++++"
      END DO
      ANN%fdata_selected=ANNOutput(ANN,ANN%xdata_selected,ANN%nselected,ANN%ndimensionsx)
      RSq= RSquared(ANN%ydata_selected,ANN%fdata_selected,ANN%nselected)
      WRITE(UNIT=6,FMT='(" ... RSquared value: ",F15.5)') RSq
      
      !find the maximum error
      maxerror=0._dp
      WRITE(6,*) "ActualActivationEnergy  PredictedActivationEnergy"
      DO i=1,ANN%nselected
         maxerror=MAX(maxerror,ABS(ANN%ydata_selected(i)-ANN%fdata_selected(i)))
         WRITE(6,*) ANN%ydata_selected(i),ANN%fdata_selected(i)
      END DO
      WRITE(6,*) "Maximum error is :",maxerror
      WRITE(6,*) "for total number of data points;",ANN%nselected
      
      IF (RSq<0.9) THEN
         WRITE(6,*) "... Poor fit obtained during training stage"
         errorstatus=1
         STOP
      END IF
      IF (ANN%TrackCorrelations) THEN
         CALL AddCorrelations(ANN,ANN%xdata_selected,ANN%ndimensionsx,ANN%nselected) !add which correlations are considered
         CALL DisplayCorrelations(ANN)
      END IF
      
      !Test the network
      purpose=2
      WRITE(6,*) "Testing the network in TrainANNBackpropagation ..."
      CALL SelectData(ANN,purpose)!Collect the data into one array
      ANN%fdata_selected=ANNOutput(ANN,ANN%xdata_selected,ANN%nselected,ANN%ndimensionsx)
      RSq= RSquared(ANN%ydata_selected,ANN%fdata_selected,ANN%nselected)
      WRITE(UNIT=6,FMT='(" ... RSquared value: ",F15.5)') RSq
      
      !find the maximum error
      maxerror=0._dp
      WRITE(6,*) "ActualActivationEnergy  PredictedActivationEnergy"
      DO i=1,ANN%nselected
         maxerror=MAX(maxerror,ABS(ANN%ydata_selected(i)-ANN%fdata_selected(i)))
         WRITE(6,*) ANN%ydata_selected(i),ANN%fdata_selected(i)
      END DO
      WRITE(6,*) "Maximum error is :",maxerror
      WRITE(6,*) "for total number of data points;",ANN%nselected
      
      IF (RSq<0.9) THEN
         WRITE(6,*) "... Poor fit obtained during testing stage"
         errorstatus=2
         STOP
      END IF
      !Validate the network
      purpose=3
      WRITE(6,*) "Validating the network in TrainANNBackpropagation ..."
      CALL SelectData(ANN,purpose)!Collect the data into one array
      ANN%fdata_selected=ANNOutput(ANN,ANN%xdata_selected,ANN%nselected,ANN%ndimensionsx)
      RSq= RSquared(ANN%ydata_selected,ANN%fdata_selected,ANN%nselected)
      WRITE(UNIT=6,FMT='(" ... RSquared value: ",F15.5)') RSq
      
      !find the maximum error
      maxerror=0._dp
      WRITE(6,*) "ActualActivationEnergy  PredictedActivationEnergy"
      DO i=1,ANN%nselected
         maxerror=MAX(maxerror,ABS(ANN%ydata_selected(i)-ANN%fdata_selected(i)))
         WRITE(6,*) ANN%ydata_selected(i),ANN%fdata_selected(i)
      END DO
      WRITE(6,*) "Maximum error is :",maxerror
      WRITE(6,*) "for total number of data points;",ANN%nselected
      
      IF (RSq<0.9) THEN
         WRITE(6,*) "... Poor fit obtained during validation stage"
         errorstatus=3
         STOP
      END IF
      
      ANN%IsFitted=.TRUE.
!WRITE(UNIT=6,FMT='("Type any key to continue:")',ADVANCE="NO")
!READ(*,*) crapchar
      STOP
   END SUBROUTINE TrainANNBackpropagation
