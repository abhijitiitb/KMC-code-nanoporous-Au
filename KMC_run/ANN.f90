   SUBROUTINE CreateANN(ANN,natoms,ndimensionsx,xdata,ydata,nmaxdata,ndata, &
      xdata_stored,ydata_stored,nmaxdata_stored,ndata_stored,ncritical_stored, &
      ftraining,ftesting,fvalidation,nneuronperlayer, &
