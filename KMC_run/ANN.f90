   SUBROUTINE PartitionANNData(ANN,iprint)
   !finds the values of ntraining, ntesting and nvalidation
   !fills the array datapurpose with a number 1, 2 or 3 
   ! 1: training data
   ! 2: testing data
   ! 3: validation data
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: ndata,ntraining,ntesting,nvalidation
      INTEGER, DIMENSION(:), POINTER :: datapurpose
      REAL(dp) :: ftraining,ftesting,fvalidation,rand1
      INTEGER :: idata1,idata,value,iprint1
      INTEGER, OPTIONAL :: iprint
      
      IF (PRESENT(iprint)) THEN
         iprint1=iprint
      ELSE
         iprint1=1
      END IF
      
      ndata=ANN%ndata
      IF (ndata==0) THEN
         WRITE(6,*) "Number of data points stored in ANN is zero"
         STOP
      END IF
      ftraining=ANN%ftraining
      ftesting=ANN%ftesting
      fvalidation=ANN%fvalidation
      IF (ftraining<=0._dp .OR. ftesting<=0._dp .OR. fvalidation<=0._dp) THEN
         WRITE(6,*) "Err>> Fraction of data for training, testing and validation not specified in PartitionANNData"
         STOP
      END IF
      ntraining=CEILING(REAL(ndata)*ftraining)
      nvalidation=FLOOR(REAL(ndata)*fvalidation)
      ntesting=ndata-ntraining-nvalidation
      ANN%ntraining=ntraining
      ANN%ntesting=ntesting
      ANN%nvalidation=nvalidation
      IF (iprint1>0) THEN
         WRITE(UNIT=6,FMT='(" Partitioning data into following sets ... ")')
         WRITE(UNIT=6,FMT='(" ... Training: ",I4)') ntraining
         WRITE(UNIT=6,FMT='(" ... Testing: ",I4)') ntesting
         WRITE(UNIT=6,FMT='(" ... Validation: ",I4)') nvalidation
      END IF
      
      IF (ASSOCIATED(ANN%datapurpose)) THEN
         IF (SIZE(ANN%datapurpose)/=ndata) THEN
            DEALLOCATE(ANN%datapurpose)
            ALLOCATE(ANN%datapurpose(ndata))
            IF (iprint1>0) WRITE(UNIT=6,FMT='(" ... creating datapurpose array of size: ",I6)') ndata
         END IF
      ELSE
         ALLOCATE(ANN%datapurpose(ndata))
         IF (iprint1>0) WRITE(UNIT=6,FMT='(" ... creating datapurpose array of size: ",I6)') ndata
      END IF
      datapurpose=>ANN%datapurpose
      
      !now choose the set randomly
      datapurpose(1:ntraining)=1
      datapurpose(ntraining+1:ntraining+ntesting)=2
      datapurpose(ntraining+ntesting+1:ndata)=3
      DO idata=1,ndata
         rand1=taus88()
         idata1=CEILING(rand1*REAL(ndata))
         value=datapurpose(idata)
         datapurpose(idata)=datapurpose(idata1)
         datapurpose(idata1)=value
      END DO
      IF (iprint1>0) WRITE(UNIT=6,FMT='(" Completed partitioning of data")')
      
   END SUBROUTINE PartitionANNData
