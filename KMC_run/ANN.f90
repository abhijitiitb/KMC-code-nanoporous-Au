   FUNCTION DataIsUnique(ANN,xdata,ydata,ndimensionsx,iprint)
   !TRUE when this data point is new to ANN, else it is false
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: ndata,ndimensionsx,i
      REAL(dp), DIMENSION(ndimensionsx) :: xdata,w
      REAL(dp) :: distance,ydata
      REAL(dp), DIMENSION(:), POINTER :: xdata1=>NULL()
      LOGICAL :: DataIsUnique
      INTEGER :: iprint1,j
      INTEGER, OPTIONAL :: iprint
      
      IF (PRESENT(iprint)) THEN
         iprint1=iprint
      ELSE
         iprint1=1
      END IF
      
      DataIsUnique=.TRUE.
      xdata1=>ANN%xdata
      w=1._dp
      IF (iprint1>0) WRITE(6,*) "Comparing distance of new point to points in ANN ..."
      DO i=1,ANN%ndata
         distance=HammingDistance(xdata,xdata1(ndimensionsx*(i-1)+1:ndimensionsx*i),ndimensionsx,w)
         IF (iprint1>0) THEN
            WRITE(UNIT=6,FMT='("[")',ADVANCE="NO")
            DO j=1,ndimensionsx
               WRITE(UNIT=6,FMT='(F3.0)',ADVANCE="NO") xdata1(ndimensionsx*(i-1)+j)
            END DO
            WRITE(UNIT=6,FMT='("] Distance:",F15.5)') distance
         END IF
         IF (distance==0._dp) THEN
            DataIsUnique=.FALSE.
            RETURN
         END IF
      END DO
      
      xdata1=>ANN%xdata_stored
      DO i=1,ANN%ndata_stored
         distance=HammingDistance(xdata,xdata1(ndimensionsx*(i-1)+1:ndimensionsx*i),ndimensionsx,w)
         IF (iprint1>0) THEN
            WRITE(UNIT=6,FMT='("[")',ADVANCE="NO")
            DO j=1,ndimensionsx
               WRITE(UNIT=6,FMT='(F3.0)',ADVANCE="NO") xdata1(ndimensionsx*(i-1)+j)
            END DO
            WRITE(UNIT=6,FMT='("] Distance:",F15.5)') distance
         END IF
         IF (distance==0._dp) THEN
            DataIsUnique=.FALSE.
            RETURN
         END IF
      END DO
   END FUNCTION DataIsUnique
