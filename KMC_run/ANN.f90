   FUNCTION ANNErrorGradient(x,errorstatus)
   !Gives the gradient with respect to the weights
      IMPLICIT NONE
      REAL(dp), DIMENSION(:), INTENT(IN) :: x
      REAL(dp), DIMENSION(SIZE(x)) :: weights
      REAL(dp), DIMENSION(SIZE(x)) :: ANNErrorGradient
      REAL(dp) :: ANNError0,ANNError1,dweight
      INTEGER :: iweight,errorstatus
      
      ANNErrorGradient=0._dp
      weights=x
      dweight=0.0001_dp
      ANNError0=ANNError(weights,errorstatus)
      DO iweight=1,ActiveNeuralNet%nweightsactive+ActiveNeuralNet%nbias
         weights(iweight)=weights(iweight)+dweight
         ANNError1=ANNError(weights,errorstatus)
         ANNErrorGradient(iweight)=(ANNError1-ANNError0)/dweight
         !WRITE(6,*) ANNError1,ANNError0,ANNErrorGradient(iweight)
         weights(iweight)=weights(iweight)-dweight
      END DO
      !WRITE(6,*) "Completed gradient calculation"
      
   END FUNCTION ANNErrorGradient
