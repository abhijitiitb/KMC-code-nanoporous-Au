   FUNCTION EliminatedWeakInteractions(ANN,ecut)
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      REAL(dp) :: ecut
      LOGICAL :: EliminatedWeakInteractions
      INTEGER :: i
      
      EliminatedWeakInteractions=.FALSE.
      DO i=1,ANN%ndimensionsx
         IF (ABS(ANN%weights(i))<ecut .AND. ABS(ANN%weights(i))>0._dp) THEN
            WRITE(*,*) "Ignoring interaction #",i
            ANN%InteractionIsActive(i)=.FALSE.
            EliminatedWeakInteractions=.TRUE.
            ANN%weights(i)=0._dp
            ANN%nweightsactive=ANN%nweightsactive-1
         END IF
      END DO
   END FUNCTION EliminatedWeakInteractions
