   SUBROUTINE ANNLayerOutput(xdata,ndimensionsx,ndata,weights,nweights,bias,nbias, &
      nneurons,activationfunction,output)
   !Purpose of this routine: finds the output from a single layer of ANN
   !xdata is the input data (1D array of size ndimensionsx*ndata)
   !ndimensionx is the dimensionality of the input to a single neuron
   !ndata is the number of data points in x
   !weights is the weights for each of the neurons in the layer i (1D array of size ndimensions*nneuronperlayer(i))
   ! in the raw form it is provided as transpose of [w(1,1) w(1,2) ....  w(1,m) w(2,1) w(2,2) ....  w(2,m) ..... w(n,1) w(n,2) ....  w(n,m)]
   !nweights is the size of weights array
   !bias is the bias for the neurons in this layer
   !nbias is the size of the bias array
   !nneurons is the number of neurons in the layer
   !activationfunction is an integer corresponding to the type of activation function to be used
   !output is the output from the single layer (1D array of size nneurons*ndata)
      IMPLICIT NONE
      INTEGER :: ndimensionsx,ndata,nweights,nbias,nneurons
      REAL(dp), DIMENSION(ndimensionsx*ndata) :: xdata
      REAL(dp), DIMENSION(nweights) :: weights
      REAL(dp), DIMENSION(nbias) :: bias
      REAL(dp), DIMENSION(nneurons*ndata) :: output
      INTEGER :: activationfunction,imin,imax,idata
      
      !Find dot product of weight matrix and input matrix
      output=PACK(MATMUL( & !pack the result into a 1D column
         RESHAPE(weights,shape=(/nneurons,ndimensionsx/),order=(/2,1/)), & !each row contains the weights for a neuron
         RESHAPE(xdata,shape=(/ndimensionsx,ndata/))), & !each column corresponds to a data point
         MASK=.TRUE.)
         
      !Add the weights
      imin=1
      imax=nbias
      DO idata=1,ndata
         output(imin:imax)=output(imin:imax)+bias(1:nbias)
         imin=imax+1
         imax=imax+nbias
      END DO
      !Activation function
      SELECT CASE (activationfunction)
      CASE(1) !LogSigmoidal
         output=LogSigmoidal(output,nneurons*ndata)
      CASE(2) !TanSigmoidal
         output=TanSigmoidal(output,nneurons*ndata)
      CASE(3) !PurelyLinear
         output=PureLinear(output,nneurons*ndata)
      CASE(4) !user defined
         output=UserFunction(output,nneurons*ndata)
      CASE DEFAULT
         WRITE(6,*) "Err>> This type of activation function is not defined:",activationfunction
         STOP
      END SELECT
   END SUBROUTINE ANNLayerOutput    
