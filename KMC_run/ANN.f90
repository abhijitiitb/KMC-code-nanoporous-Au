   SUBROUTINE SelectData(ANN,purpose,iprint)
   !Collect data of purpose given by purpose
   !purpose can be for (1)training (2)testing or (3)validation
   !this data is stored in ydata_selected and xdata_selected of size nselected
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER, DIMENSION(:), POINTER :: datapurpose
      REAL(dp), DIMENSION(:), POINTER :: xdata,ydata,xdata_selected,ydata_selected
      INTEGER :: nselected,purpose,iselected,idata,ndata,ndimensionsx,iprint1
      INTEGER, OPTIONAL :: iprint
      
      IF (PRESENT(iprint)) THEN
         iprint1=iprint
      ELSE
         iprint1=1
      END IF
      
      IF (iprint1>0) THEN
         WRITE(UNIT=6,FMT='(" Transferring data into xdata_selected for purpose: ")',ADVANCE="NO")
         SELECT CASE(purpose)
         CASE(1); WRITE(6,*) "TRAINING"
         CASE(2); WRITE(6,*) "TESTING"
         CASE(3); WRITE(6,*) "VALIDATION"
         END SELECT
      END IF
      !--------------------------------------------------------------------
      !Initial checks and assigment of nselected
      ndimensionsx=ANN%ndimensionsx
      IF (ndimensionsx==0) THEN
         WRITE(6,*) "Err>> SelectData: Number of dimensions associated with input has to be provided"
         STOP
      END IF
      ndata=ANN%ndata
      IF (ndata==0) THEN
         WRITE(6,*) "Err>> SelectData: Number of input data points has to be provided"
         STOP
      END IF
      IF (ASSOCIATED(ANN%xdata)) THEN
         IF (ANN%ndata==0 .OR. SIZE(ANN%xdata)==0) THEN
            WRITE(6,*) "Err>> SelectData: Number of xdata points is zero"
            STOP
         END IF
      ELSE
         WRITE(6,*) "Err>> SelectData: Add xdata to ANN"
         STOP
      END IF
      IF (ASSOCIATED(ANN%ydata)) THEN
         IF (SIZE(ANN%ydata)==0) THEN
            WRITE(6,*) "Err>> SelectData: Number of ydata points is zero"
            STOP
         END IF
      ELSE
         WRITE(6,*) "Err>> SelectData: Add ysdata to ANN"
         STOP
      END IF
      
      SELECT CASE(purpose)
      CASE(1); nselected=ANN%ntraining
      CASE(2); nselected=ANN%ntesting
      CASE(3); nselected=ANN%nvalidation
      CASE DEFAULT
         WRITE(6,*) "Err>> SelectData: Type of data purpose unknown"
         STOP
      END SELECT
      IF (nselected==0) THEN
         WRITE(6,*) "Err>> SelectData: Original data has not been partitioned for ANN training/testing/validation"
         STOP
      !otherwise we can safely assume that datapurpose is also defined
      END IF
      ANN%nselected=nselected
      IF (ASSOCIATED(ANN%xdata_selected)) THEN
         IF (SIZE(ANN%xdata_selected)/=ndimensionsx*nselected) THEN
            DEALLOCATE(ANN%xdata_selected)
            ALLOCATE(ANN%xdata_selected(ndimensionsx*nselected))
         END IF
      ELSE
         ALLOCATE(ANN%xdata_selected(ndimensionsx*nselected))
      END IF
      IF (ASSOCIATED(ANN%ydata_selected)) THEN
         IF (SIZE(ANN%ydata_selected)/=nselected) THEN
            DEALLOCATE(ANN%ydata_selected)
            ALLOCATE(ANN%ydata_selected(nselected))
         END IF
      ELSE
         ALLOCATE(ANN%ydata_selected(nselected))
      END IF
      IF (ASSOCIATED(ANN%fdata_selected)) THEN
         IF (SIZE(ANN%fdata_selected)/=nselected) THEN
            DEALLOCATE(ANN%fdata_selected)
            ALLOCATE(ANN%fdata_selected(nselected))
         END IF
      ELSE
         ALLOCATE(ANN%fdata_selected(nselected))
      END IF
      IF (iprint1>0) THEN
         WRITE(UNIT=6,FMT='(" ... xdata_selected has size: ",I6)') ndimensionsx*nselected
         WRITE(UNIT=6,FMT='(" ... ydata_selected has size: ",I6)') nselected
         WRITE(UNIT=6,FMT='(" ... fdata_selected has size: ",I6)') nselected
      END IF
      xdata_selected=>ANN%xdata_selected
      ydata_selected=>ANN%ydata_selected
      xdata=>ANN%xdata
      ydata=>ANN%ydata
      datapurpose=>ANN%datapurpose
      
      !--------------------------------------------------------------------
      !Now fill the data of type datapurpose into xdata_selectde and ydata_selected
      iselected=0
      DO idata=1,ndata
         !WRITE(*,*) idata,datapurpose(idata)
         IF (datapurpose(idata)==purpose) THEN !this gets selected
            iselected=iselected+1

            ydata_selected(iselected)=ydata(idata)
            xdata_selected((iselected-1)*ndimensionsx+1:iselected*ndimensionsx)= &
               xdata((idata-1)*ndimensionsx+1:idata*ndimensionsx)
            !WRITE(*,*) iselected
         END IF
      END DO
      IF (iprint1>0) WRITE(6,*) "Transferred data into xdata_selected"
   END SUBROUTINE SelectData
