   SUBROUTINE GetActiveInteractions(ANN)
   !find interactions that are important - these are interactions with large weight
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER, DIMENSION(:), POINTER :: InteractionCount
      LOGICAL, DIMENSION(:), POINTER :: InteractionIsActive=>NULL()
      REAL(dp), DIMENSION(:), POINTER :: xdata=>NULL()
      REAL(dp), DIMENSION(ANN%ndimensionsx) :: xdata1
      INTEGER :: idata,nweights,i,ndimensionsx
      
      InteractionIsActive=>ANN%InteractionIsActive
      InteractionCount=>ANN%InteractionCount
      
      !check how many times an interaction is switched on
      xdata=>ANN%xdata_selected
      InteractionCount=0
      ndimensionsx=ANN%ndimensionsx
      DO idata=1,ANN%nselected
         xdata1=xdata(ndimensionsx*(idata-1)+1:ndimensionsx*idata)
         DO i=1,ndimensionsx
            IF (xdata1(i)/=0._dp) InteractionCount(i)=InteractionCount(i)+1
         END DO
      END DO
      
      !if interaction is on more than 2 times and off more than 2 times then it is active
      InteractionIsActive=.FALSE.
      nweights=0
      DO i=1,ndimensionsx
         InteractionIsActive(i)=InteractionCount(i)>0 .AND. InteractionCount(i)<ANN%nselected
         IF (InteractionIsActive(i)) nweights=nweights+1
      END DO
      ANN%nweightsactive=nweights
      WRITE(6,*) "Number of active interactions:",nweights
      
      !now modify the size of the weight array and train for active interactions
   END SUBROUTINE GetActiveInteractions
