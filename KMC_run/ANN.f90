   FUNCTION ANNOutput(ANN,xdata,ndata,ndimensionsx)
   !weights,nweights,nbias,nneuronperlayer,nlayers,activationfunction)
   !Finds the output from ANN for the weights provided in the network
   !xdata is the raw input
   !nweights is the number of weights in the ANN
   !nbias is the number of bias in the ANN
      IMPLICIT NONE
      TYPE(NeuralNet), POINTER :: ANN
      INTEGER :: ilayer,nlayers,ndata,ndimensionsx,nweights,nbias,activationfunction
      REAL(dp), DIMENSION(:), TARGET :: xdata
      REAL(dp), DIMENSION(:), POINTER :: weights
      INTEGER, DIMENSION(:), POINTER :: nneuronperlayer
      REAL(dp), DIMENSION(ndata) :: ANNOutput
      
      !Allocatable arrays
      REAL(dp), DIMENSION(:), POINTER :: LayerInput,LayerOutput,weights1,bias1
      !Used for layer calculations
      INTEGER :: nneurons1,nweights1,nbias1,ndata1,ndimx1
      INTEGER :: iweightmin,iweightmax,ibiasmin,ibiasmax
      
      ANNOutput=0._dp
      weights=>ANN%weights
      nweights=ANN%nweights
      nbias=ANN%nbias
      nlayers=ANN%nlayers
      nneuronperlayer=>ANN%nneuronperlayer
      
      LayerInput=>xdata(1:ndimensionsx*ndata)
      ndata1=ndata
      
      !Find range of the weight vector that needs to be used
      iweightmin=1
      ibiasmin=nweights+1
      ndimx1=ndimensionsx
      
      DO ilayer=1,nlayers
         activationfunction=ANN%activationfunction(ilayer)
         nneurons1=nneuronperlayer(ilayer)
         nweights1=nneurons1*ndimx1 !number of weights in this layer
         iweightmax=iweightmin-1+nweights1
         nbias1=nneurons1 !number of bias in this layer
         ibiasmax=ibiasmin-1+nbias1
         weights1=>weights(iweightmin:iweightmax)
         bias1=>weights(ibiasmin:ibiasmax)
         ALLOCATE(LayerOutput(nneurons1*ndata))
         CALL ANNLayerOutput(LayerInput,ndimx1,ndata1,weights1,nweights1,bias1,nbias1, &
            nneurons1,activationfunction,LayerOutput)
         
         !Setup the input/output arrays
         IF (ilayer>1) DEALLOCATE(LayerInput)
         LayerInput=>LayerOutput
         NULLIFY(LayerOutput)
         
         ndimx1=nneurons1
         iweightmin=iweightmax+1
         ibiasmin=ibiasmax+1
      END DO
      
      ANNOutput(1:ndata)=LayerInput(1:ndata)
      DEALLOCATE(LayerInput) !release the memory
   END FUNCTION ANNOutput
